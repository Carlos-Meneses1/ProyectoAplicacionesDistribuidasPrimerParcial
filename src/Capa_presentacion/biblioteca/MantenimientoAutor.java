    private void btnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarActionPerformed
        // TODO add your handling code here:
        if (!validarCampos()) {
            return;
        }

        int r = negocio.insertar(
                txtCodigo.getText(),
                txtNombre.getText().trim(),
                txtApellido.getText().trim()
        );

        if (r == 1) {
            JOptionPane.showMessageDialog(this, "Autor insertado correctamente");
            cargarTabla();
            nuevoCodigo();
        } else {
            JOptionPane.showMessageDialog(this, "Error al insertar el autor");
        }
    }//GEN-LAST:event_btnInsertarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
        if (!validarCampos()) {
            return;
        }

        int r = negocio.modificar(
                txtCodigo.getText(),
                txtNombre.getText().trim(),
                txtApellido.getText().trim()
        );

        if (r == 1) {
            JOptionPane.showMessageDialog(this, "Autor modificado correctamente");
            cargarTabla();
            nuevoCodigo();
        } else {
            JOptionPane.showMessageDialog(this, "No se pudo modificar el autor");
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        int opcion = JOptionPane.showConfirmDialog(
                this,
                "¿Seguro que desea eliminar este autor?",
                "Confirmar",
                JOptionPane.YES_NO_OPTION
        );

        if (opcion == JOptionPane.YES_OPTION) {
            int r = negocio.eliminar(txtCodigo.getText());

            if (r == 1) {
                JOptionPane.showMessageDialog(this, "Autor eliminado correctamente");
                cargarTabla();
                nuevoCodigo();
            } else {
                JOptionPane.showMessageDialog(this, "No se pudo eliminar el autor");
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        String codigo = JOptionPane.showInputDialog(this, "Ingrese el código del autor:");

        if (codigo != null && !codigo.trim().isEmpty()) {
            Autor a = negocio.buscar(codigo.trim());

            if (a != null) {
                txtCodigo.setText(a.getCodigo());
                txtNombre.setText(a.getNombre());
                txtApellido.setText(a.getApellido());
            } else {
                JOptionPane.showMessageDialog(this, "No existe el autor");
            }
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void tblAutorMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblAutorMouseClicked
        // TODO add your handling code here:
        int fila = tblAutor.getSelectedRow();

        if (fila >= 0) {
            txtCodigo.setText(tblAutor.getValueAt(fila, 0).toString());
            txtNombre.setText(tblAutor.getValueAt(fila, 1).toString());
            txtApellido.setText(tblAutor.getValueAt(fila, 2).toString());
        }
    }//GEN-LAST:event_tblAutorMouseClicked

