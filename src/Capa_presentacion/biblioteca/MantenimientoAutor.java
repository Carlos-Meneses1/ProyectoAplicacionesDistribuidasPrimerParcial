    private void nuevoCodigo() {
        txtCodigo.setText(negocio.siguienteCodigo());
        txtNombre.setText("");
        txtApellido.setText("");
        txtNombre.requestFocus();
    }

    private void cargarTabla() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Código");
        modelo.addColumn("Nombre");
        modelo.addColumn("Apellido");

        List<Autor> lista = negocio.listar();

        if (lista != null) {
            for (Autor a : lista) {
                modelo.addRow(new Object[]{
                    a.getCodigo(),
                    a.getNombre(),
                    a.getApellido()
                });
            }
        }

        tblAutor.setModel(modelo);
    }

    private boolean validarCampos() {
        if (txtNombre.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese el nombre del autor");
            txtNombre.requestFocus();
            return false;
        }

        if (txtApellido.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese el apellido del autor");
            txtApellido.requestFocus();
            return false;
        }

        return true;
    }

