private void limpiar() {
    txtIsbn.setText("");
    txtTitulo.setText("");
    txtValorPrestamo.setText("");

    if (cmbAutor.getItemCount() > 0) {
        cmbAutor.setSelectedIndex(0);
    }

    txtIsbn.requestFocus();
}

private void cargarComboAutores() {
    cmbAutor.removeAllItems();

    List<Autor> lista = negocio.listarAutores();

    if (lista != null) {
        for (Autor a : lista) {
            cmbAutor.addItem(a.getCodigo() + " - " + a.getNombre() + " " + a.getApellido());
        }
    }
}

private String obtenerCodigoAutorSeleccionado() {
    String texto = cmbAutor.getSelectedItem().toString();
    String codigo = texto.substring(0, texto.indexOf(" - "));
    return codigo;
}

private void seleccionarAutor(String codigo) {
    for (int i = 0; i < cmbAutor.getItemCount(); i++) {
        String item = cmbAutor.getItemAt(i).toString();

        if (item.startsWith(codigo + " - ")) {
            cmbAutor.setSelectedIndex(i);
            return;
        }
    }
}

private void cargarTabla() {
    DefaultTableModel modelo = new DefaultTableModel();
    modelo.addColumn("ISBN");
    modelo.addColumn("Título");
    modelo.addColumn("Autor");
    modelo.addColumn("Valor préstamo");

    List<Libro> lista = negocio.listar();

    if (lista != null) {
        for (Libro l : lista) {
            String autor = "";

            if (l.getCodAutor() != null) {
                autor = l.getCodAutor().getNombre() + " " + l.getCodAutor().getApellido();
            }

            modelo.addRow(new Object[]{
                l.getIsbn(),
                l.getTitulo(),
                autor,
                l.getValorPrestamo()
            });
        }
    }

    tblLibro.setModel(modelo);
}

private boolean validarCampos() {
    if (txtIsbn.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Ingrese el ISBN");
        txtIsbn.requestFocus();
        return false;
    }

    if (txtTitulo.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Ingrese el título del libro");
        txtTitulo.requestFocus();
        return false;
    }

    if (cmbAutor.getSelectedItem() == null) {
        JOptionPane.showMessageDialog(this, "Primero debe registrar un autor");
        return false;
    }

    if (txtValorPrestamo.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Ingrese el valor del préstamo");
        txtValorPrestamo.requestFocus();
        return false;
    }

    try {
        new BigDecimal(txtValorPrestamo.getText().trim());
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "El valor del préstamo debe ser numérico");
        txtValorPrestamo.requestFocus();
        return false;
    }

    return true;
}
