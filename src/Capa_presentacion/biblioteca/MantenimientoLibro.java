    private void btnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarActionPerformed
        // TODO add your handling code here:
         if (!validarCampos()) {
        return;
    }

    String codAutor = obtenerCodigoAutorSeleccionado();

    int r = negocio.insertar(
            txtIsbn.getText().trim(),
            txtTitulo.getText().trim(),
            codAutor,
            new BigDecimal(txtValorPrestamo.getText().trim())
    );

    if (r == 1) {
        JOptionPane.showMessageDialog(this, "Libro insertado correctamente");
        cargarTabla();
        limpiar();
    } else {
        JOptionPane.showMessageDialog(this, "Error al insertar el libro");
    }
    }//GEN-LAST:event_btnInsertarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
        if (!validarCampos()) {
        return;
    }

    String codAutor = obtenerCodigoAutorSeleccionado();

    int r = negocio.modificar(
            txtIsbn.getText().trim(),
            txtTitulo.getText().trim(),
            codAutor,
            new BigDecimal(txtValorPrestamo.getText().trim())
    );

    if (r == 1) {
        JOptionPane.showMessageDialog(this, "Libro modificado correctamente");
        cargarTabla();
        limpiar();
    } else {
        JOptionPane.showMessageDialog(this, "No se pudo modificar el libro");
    }

    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        int opcion = JOptionPane.showConfirmDialog(
            this,
            "¿Seguro que desea eliminar este libro?",
            "Confirmar",
            JOptionPane.YES_NO_OPTION
    );

    if (opcion == JOptionPane.YES_OPTION) {
        int r = negocio.eliminar(txtIsbn.getText().trim());

        if (r == 1) {
            JOptionPane.showMessageDialog(this, "Libro eliminado correctamente");
            cargarTabla();
            limpiar();
        } else {
            JOptionPane.showMessageDialog(this, "No se pudo eliminar el libro");
        }
    }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        String isbn = JOptionPane.showInputDialog(this, "Ingrese el ISBN del libro:");

    if (isbn != null && !isbn.trim().isEmpty()) {
        Libro l = negocio.buscar(isbn.trim());

        if (l != null) {
            txtIsbn.setText(l.getIsbn());
            txtTitulo.setText(l.getTitulo());

            if (l.getValorPrestamo() != null) {
                txtValorPrestamo.setText(l.getValorPrestamo().toString());
            } else {
                txtValorPrestamo.setText("");
            }

            if (l.getCodAutor() != null) {
                seleccionarAutor(l.getCodAutor().getCodigo());
            }

        } else {
            JOptionPane.showMessageDialog(this, "No existe el libro");
        }
    }
    }//GEN-LAST:event_btnBuscarActionPerformed

