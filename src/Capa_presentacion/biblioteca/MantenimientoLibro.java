    private void tblLibroMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblLibroMouseClicked
        // TODO add your handling code here:
        int fila = tblLibro.getSelectedRow();

    if (fila >= 0) {
        String isbn = tblLibro.getValueAt(fila, 0).toString();

        Libro l = negocio.buscar(isbn);

        if (l != null) {
            txtIsbn.setText(l.getIsbn());
            txtTitulo.setText(l.getTitulo());

            if (l.getValorPrestamo() != null) {
                txtValorPrestamo.setText(l.getValorPrestamo().toString());
            } else {
                txtValorPrestamo.setText("");
            }

            if (l.getCodAutor() != null) {
                seleccionarAutor(l.getCodAutor().getCodigo());
            }
        }
    }
    }//GEN-LAST:event_tblLibroMouseClicked

