    private void nuevoCodigo() {
        txtCodigo.setText("");
        txtNombre.setText("");

        if (cmbTipoCuenta.getItemCount() > 0) {
            cmbTipoCuenta.setSelectedIndex(0);
        }

        txtCodigo.requestFocus();
    }

    private void cargarComboTipoCuenta() {
        cmbTipoCuenta.removeAllItems();

        List<TipoCuenta> lista = negocio.listarTiposCuenta();

        if (lista != null) {
            for (TipoCuenta t : lista) {
                cmbTipoCuenta.addItem(t.getCodigo() + " - " + t.getNombre());
            }
        }
    }

    private String obtenerCodigoTipoSeleccionado() {
        String texto = cmbTipoCuenta.getSelectedItem().toString();
        return texto.substring(0, texto.indexOf(" - "));
    }

    private void seleccionarTipoCuenta(String codigo) {
        for (int i = 0; i < cmbTipoCuenta.getItemCount(); i++) {
            String item = cmbTipoCuenta.getItemAt(i).toString();

            if (item.startsWith(codigo + " - ")) {
                cmbTipoCuenta.setSelectedIndex(i);
                return;
            }
        }
    }

    private void cargarTabla() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Código");
        modelo.addColumn("Nombre");
        modelo.addColumn("Tipo cuenta");

        List<Cuenta> lista = negocio.listar();

        if (lista != null) {
            for (Cuenta c : lista) {
                String tipo = "";

                if (c.getCodTipo() != null) {
                    tipo = c.getCodTipo().getNombre();
                }

                modelo.addRow(new Object[]{
                    c.getCodigo(),
                    c.getNombre(),
                    tipo
                });
            }
        }

        tblCuenta.setModel(modelo);
    }

    private boolean validarCampos() {
        if (txtCodigo.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese el código de la cuenta");
            txtCodigo.requestFocus();
            return false;
        }

        if (txtNombre.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese el nombre de la cuenta");
            txtNombre.requestFocus();
            return false;
        }

        if (cmbTipoCuenta.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(this, "Primero debe registrar un tipo de cuenta");
            return false;
        }

        return true;
    }

