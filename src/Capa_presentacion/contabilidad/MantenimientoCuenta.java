    private void btnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarActionPerformed
        // TODO add your handling code here:
        if (!validarCampos()) {
            return;
        }

        String codTipo = obtenerCodigoTipoSeleccionado();

        int r = negocio.insertar(
                txtCodigo.getText(),
                txtNombre.getText().trim(),
                codTipo
        );

        if (r == 1) {
            JOptionPane.showMessageDialog(this, "Cuenta insertada correctamente");
            cargarTabla();
            nuevoCodigo();
        } else {
            JOptionPane.showMessageDialog(this, "Error al insertar la cuenta");
        }
    }//GEN-LAST:event_btnInsertarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
        if (!validarCampos()) {
            return;
        }

        String codTipo = obtenerCodigoTipoSeleccionado();

        int r = negocio.modificar(
                txtCodigo.getText(),
                txtNombre.getText().trim(),
                codTipo
        );

        if (r == 1) {
            JOptionPane.showMessageDialog(this, "Cuenta modificada correctamente");
            cargarTabla();
            nuevoCodigo();
        } else {
            JOptionPane.showMessageDialog(this, "No se pudo modificar la cuenta");
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        int opcion = JOptionPane.showConfirmDialog(
                this,
                "¿Seguro que desea eliminar esta cuenta?",
                "Confirmar",
                JOptionPane.YES_NO_OPTION
        );

        if (opcion == JOptionPane.YES_OPTION) {
            int r = negocio.eliminar(txtCodigo.getText());

            if (r == 1) {
                JOptionPane.showMessageDialog(this, "Cuenta eliminada correctamente");
                cargarTabla();
                nuevoCodigo();
            } else {
                JOptionPane.showMessageDialog(this, "No se pudo eliminar la cuenta");
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        String codigo = JOptionPane.showInputDialog(this, "Ingrese el código de la cuenta:");

        if (codigo != null && !codigo.trim().isEmpty()) {
            Cuenta c = negocio.buscar(codigo.trim());

            if (c != null) {
                txtCodigo.setText(c.getCodigo());
                txtNombre.setText(c.getNombre());

                if (c.getCodTipo() != null) {
                    seleccionarTipoCuenta(c.getCodTipo().getCodigo());
                }

            } else {
                JOptionPane.showMessageDialog(this, "No existe la cuenta");
            }
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void tblCuentaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCuentaMouseClicked
        // TODO add your handling code here:
        int fila = tblCuenta.getSelectedRow();

        if (fila >= 0) {
            String codigo = tblCuenta.getValueAt(fila, 0).toString();

            Cuenta c = negocio.buscar(codigo);

            if (c != null) {
                txtCodigo.setText(c.getCodigo());
                txtNombre.setText(c.getNombre());

                if (c.getCodTipo() != null) {
                    seleccionarTipoCuenta(c.getCodTipo().getCodigo());
                }
            }
        }
    }//GEN-LAST:event_tblCuentaMouseClicked

