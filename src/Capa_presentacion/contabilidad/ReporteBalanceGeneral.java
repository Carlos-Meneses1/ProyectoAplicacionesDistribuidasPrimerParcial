    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
         try {
        String desde = txtDesde.getText().trim();
        String hasta = txtHasta.getText().trim();

        if (desde.isEmpty() || hasta.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Ingrese fecha desde y fecha hasta");
            return;
        }

        java.util.List<Object[]> lista = negocio.reporteBalanceGeneral(desde, hasta);

        javax.swing.table.DefaultTableModel modelo = new javax.swing.table.DefaultTableModel();
        modelo.addColumn("Tipo cuenta");
        modelo.addColumn("Cuenta");
        modelo.addColumn("Debe");
        modelo.addColumn("Haber");
        modelo.addColumn("Saldo");

        for (Object[] fila : lista) {
            modelo.addRow(new Object[]{
                fila[0],
                fila[1],
                fila[2],
                fila[3],
                fila[4]
            });
        }

        tblReporte.setModel(modelo);

    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Error al generar reporte: " + e.getMessage());
    }
   
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnImprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnImprimirActionPerformed
        // TODO add your handling code here:
        try {
    if (tblReporte.getRowCount() == 0) {
        JOptionPane.showMessageDialog(this, "Primero debe buscar datos para imprimir");
        return;
    }

    boolean imprimir = tblReporte.print(
            JTable.PrintMode.FIT_WIDTH,
            new java.text.MessageFormat("Balance General"),
            new java.text.MessageFormat("Página {0}")
    );

    if (imprimir) {
        JOptionPane.showMessageDialog(this, "Reporte enviado a impresión");
    }

} catch (Exception e) {
    JOptionPane.showMessageDialog(this, "Error al imprimir: " + e.getMessage());
}
    }//GEN-LAST:event_btnImprimirActionPerformed

