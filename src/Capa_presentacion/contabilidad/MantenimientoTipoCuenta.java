    private void btnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarActionPerformed
        // TODO add your handling code here:
        if (!validarCampos()) {
            return;
        }

        int r = negocio.insertar(
                txtCodigo.getText(),
                txtNombre.getText().trim()
        );

        if (r == 1) {
            JOptionPane.showMessageDialog(this, "Tipo de cuenta insertado correctamente");
            cargarTabla();
            nuevoCodigo();
        } else {
            JOptionPane.showMessageDialog(this, "Error al insertar");
        }
    }//GEN-LAST:event_btnInsertarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
        if (!validarCampos()) {
            return;
        }

        int r = negocio.modificar(
                txtCodigo.getText(),
                txtNombre.getText().trim()
        );

        if (r == 1) {
            JOptionPane.showMessageDialog(this, "Tipo de cuenta modificado correctamente");
            cargarTabla();
            nuevoCodigo();
        } else {
            JOptionPane.showMessageDialog(this, "No se pudo modificar");
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        int opcion = JOptionPane.showConfirmDialog(
                this,
                "¿Seguro que desea eliminar este tipo de cuenta?",
                "Confirmar",
                JOptionPane.YES_NO_OPTION
        );

        if (opcion == JOptionPane.YES_OPTION) {
            int r = negocio.eliminar(txtCodigo.getText());

            if (r == 1) {
                JOptionPane.showMessageDialog(this, "Tipo de cuenta eliminado correctamente");
                cargarTabla();
                nuevoCodigo();
            } else {
                JOptionPane.showMessageDialog(this, "No se pudo eliminar");
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        String codigo = JOptionPane.showInputDialog(this, "Ingrese el código:");

        if (codigo != null && !codigo.trim().isEmpty()) {
            TipoCuenta t = negocio.buscar(codigo.trim());

            if (t != null) {
                txtCodigo.setText(t.getCodigo());
                txtNombre.setText(t.getNombre());
            } else {
                JOptionPane.showMessageDialog(this, "No existe el tipo de cuenta");
            }
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void tblTipoCuentaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblTipoCuentaMouseClicked
        // TODO add your handling code here:
        int fila = tblTipoCuenta.getSelectedRow();

        if (fila >= 0) {
            txtCodigo.setText(tblTipoCuenta.getValueAt(fila, 0).toString());
            txtNombre.setText(tblTipoCuenta.getValueAt(fila, 1).toString());
        }
    }//GEN-LAST:event_tblTipoCuentaMouseClicked

