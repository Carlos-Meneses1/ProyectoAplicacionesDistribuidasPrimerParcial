    private void nuevoCodigo() {
        txtCodigo.setText(negocio.siguienteCodigo());
        txtNombre.setText("");
        txtNombre.requestFocus();
    }

    private void cargarTabla() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Código");
        modelo.addColumn("Nombre");

        List<TipoCuenta> lista = negocio.listar();

        if (lista != null) {
            for (TipoCuenta t : lista) {
                modelo.addRow(new Object[]{
                    t.getCodigo(),
                    t.getNombre()
                });
            }
        }

        tblTipoCuenta.setModel(modelo);
    }

    private boolean validarCampos() {
        if (txtNombre.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese el nombre del tipo de cuenta");
            txtNombre.requestFocus();
            return false;
        }

        return true;
    }

