    private void tblDetalle1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDetalle1MouseClicked
        // TODO add your handling code here:
        int fila = tblDetalle1.getSelectedRow();
        if (fila >= 0) {
            txtNumero.setText(tblDetalle1.getValueAt(fila, 0).toString());
            txtFecha.setText(tblDetalle1.getValueAt(fila, 1).toString());
            txtResponsable.setText(tblDetalle1.getValueAt(fila, 2).toString());
            String numero = tblDetalle1.getValueAt(fila, 0).toString();
            cargarTablaDet(numero);
            editar = true;
        }
    }//GEN-LAST:event_tblDetalle1MouseClicked

