    private void cargarCombos() {
        cmbActivo.removeAllItems();
        java.util.List<Capa_persistencia.ActivoMant> activos = negocio.listarActivos();
        if (activos != null) {
            for (Capa_persistencia.ActivoMant a : activos) {
                cmbActivo.addItem(a.getCodigo() + " - " + a.getNombre());
            }
        }
        cmbActividad.removeAllItems();
        java.util.List<Capa_persistencia.Actividad> actividades = negocio.listarActividades();
        if (actividades != null) {
            for (Capa_persistencia.Actividad a : actividades) {
                cmbActividad.addItem(a.getCodigo() + " - " + a.getNombre());
            }
        }
    }

    private void cargarTablaCab() {
        javax.swing.table.DefaultTableModel model = new javax.swing.table.DefaultTableModel(
                new String[]{"Numero", "Fecha", "Responsable"}, 0);
        java.util.List<Capa_persistencia.CabMantenimiento> lista = negocio.listarCab();
        if (lista != null) {
            for (Capa_persistencia.CabMantenimiento c : lista) {
                String fecha = "";
                if (c.getFecha() != null) {
                    java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("dd/MM/yyyy");
                    fecha = sdf.format(c.getFecha());
                }
                model.addRow(new Object[]{c.getNumero(), fecha, c.getResponsable()});
            }
        }
        tblDetalle1.setModel(model);
        tblDetalle1.revalidate();
        tblDetalle1.repaint();
    }

    private void cargarTablaDet(String numero) {
        javax.swing.table.DefaultTableModel model = new javax.swing.table.DefaultTableModel(
                new String[]{"ID", "Activo", "Actividad", "Valor"}, 0);
        java.util.List<Capa_persistencia.DetMantenimiento> lista = negocio.listarDet(numero);
        if (lista != null) {
            for (Capa_persistencia.DetMantenimiento d : lista) {
                model.addRow(new Object[]{
                    d.getIdDet(),
                    d.getCodActivo() != null ? d.getCodActivo().getNombre() : "",
                    d.getCodActividad() != null ? d.getCodActividad().getNombre() : "",
                    d.getValor()
                });
            }
        }
        tblDetalle.setModel(model);
    }

