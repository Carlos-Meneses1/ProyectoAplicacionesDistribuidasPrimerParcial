    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (cmbActivo.getSelectedItem() == null || cmbActividad.getSelectedItem() == null) {
            javax.swing.JOptionPane.showMessageDialog(this, "Seleccione activo y actividad");
            return;
        }
        String valorTxt = txtValor.getText().trim();
        if (valorTxt.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Ingrese el valor");
            return;
        }
        try {
            java.math.BigDecimal valor = new java.math.BigDecimal(valorTxt);
            String codActivo = cmbActivo.getSelectedItem().toString().split(" - ")[0];
            String codActividad = cmbActividad.getSelectedItem().toString().split(" - ")[0];
            Capa_persistencia.DetMantenimiento det = new Capa_persistencia.DetMantenimiento();
            det.setIdDet(negocio.siguienteIdDet().add(new java.math.BigDecimal(detallesTemp.size())));
            for (Capa_persistencia.ActivoMant a : negocio.listarActivos()) {
                if (a.getCodigo().equals(codActivo)) {
                    det.setCodActivo(a);
                    break;
                }
            }
            for (Capa_persistencia.Actividad a : negocio.listarActividades()) {
                if (a.getCodigo().equals(codActividad)) {
                    det.setCodActividad(a);
                    break;
                }
            }
            det.setValor(valor);
            detallesTemp.add(det);
            cargarTablaDetTemp();
            txtValor.setText("");
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Valor inválido");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String numero = txtNumero.getText().trim();
        String fecha = txtFecha.getText().trim();
        String responsable = txtResponsable.getText().trim();
        if (fecha.isEmpty() || responsable.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Complete fecha y responsable");
            return;
        }
        if (detallesTemp.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Agregue al menos un detalle");
            return;
        }
        java.math.BigDecimal total = java.math.BigDecimal.ZERO;
        for (Capa_persistencia.DetMantenimiento d : detallesTemp) {
            total = total.add(d.getValor());
        }
        try {
            java.util.Date fechaDate = new java.text.SimpleDateFormat("dd/MM/yyyy").parse(fecha);
            int res = negocio.insertarCabConIntegracion(numero, fechaDate, responsable,
                    detallesTemp, total);
            if (res == 1) {
                javax.swing.JOptionPane.showMessageDialog(this, "Guardado y asiento contable generado");
                detallesTemp.clear();
                txtNumero.setText(negocio.siguienteNumero());
                java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("dd/MM/yyyy");
                txtFecha.setText(sdf.format(new java.util.Date()));
                txtResponsable.setText("");
                txtValor.setText("");
                editar = false;
                cargarTablaCab();
                cargarTablaDetTemp();
            } else if (res == -2) {
                javax.swing.JOptionPane.showMessageDialog(this, "No se pudo guardar. Revise que existan las cuentas: Gasto Mantenimiento y Bancos");
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Error al guardar");
            }
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());

        }
    }//GEN-LAST:event_jButton2ActionPerformed

