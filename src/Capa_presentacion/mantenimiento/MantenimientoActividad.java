    private void cargarTabla() {
        javax.swing.table.DefaultTableModel model = new javax.swing.table.DefaultTableModel(
                new String[]{"Código", "Nombre"}, 0);
        java.util.List<Capa_persistencia.Actividad> lista = negocio.listar();
        if (lista != null) {
            for (Capa_persistencia.Actividad a : lista) {
                model.addRow(new Object[]{a.getCodigo(), a.getNombre()});
            }
        }
        tblActividad.setModel(model);
    }

    private void btnNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevoActionPerformed
        // TODO add your handling code here:
        String codigo = txtCodigo.getText().trim();

        String nombre = txtNombre.getText().trim();

        if (nombre.isEmpty()) {

            javax.swing.JOptionPane.showMessageDialog(this, "Ingrese el nombre");

            return;
        }

        int resultado = negocio.insertar(codigo, nombre);

        if (resultado == 1) {

            javax.swing.JOptionPane.showMessageDialog(this, "Insertado correctamente");

            txtCodigo.setText(negocio.siguienteCodigo());

            txtNombre.setText("");

            cargarTabla();

        } else {

            javax.swing.JOptionPane.showMessageDialog(this, "Error al insertar");
        }
    }//GEN-LAST:event_btnNuevoActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
        String codigo = txtCodigo.getText().trim();

        String nombre = txtNombre.getText().trim();

        if (codigo.isEmpty()) {

            javax.swing.JOptionPane.showMessageDialog(this, "Seleccione un registro");

            return;
        }

        if (nombre.isEmpty()) {

            javax.swing.JOptionPane.showMessageDialog(this, "Ingrese el nombre");

            return;
        }

        int resultado = negocio.modificar(codigo, nombre);

        if (resultado == 1) {

            javax.swing.JOptionPane.showMessageDialog(this, "Modificado correctamente");

            txtCodigo.setText(negocio.siguienteCodigo());

            txtNombre.setText("");

            editar = false;

            cargarTabla();

        } else {

            javax.swing.JOptionPane.showMessageDialog(this, "Error al modificar");
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        String codigo = txtCodigo.getText().trim();
        if (codigo.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Seleccione un registro");
            return;
        }
        int confirm = javax.swing.JOptionPane.showConfirmDialog(
                this, "¿Eliminar este registro?", "Confirmar",
                javax.swing.JOptionPane.YES_NO_OPTION);
        if (confirm == javax.swing.JOptionPane.YES_OPTION) {
            int resultado = negocio.eliminar(codigo);
            if (resultado == 1) {
                javax.swing.JOptionPane.showMessageDialog(this, "Eliminado");
                txtCodigo.setText("");
                txtNombre.setText("");
                cargarTabla();
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Error al eliminar");
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        String codigo = javax.swing.JOptionPane.showInputDialog(this, "Ingrese el código:");

        if (codigo != null && !codigo.trim().isEmpty()) {
            Capa_persistencia.Actividad a = negocio.buscar(codigo.trim());

            if (a != null) {
                txtCodigo.setText(a.getCodigo());
                txtNombre.setText(a.getNombre());
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "No existe la actividad");
            }
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void tblActividadMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblActividadMouseClicked
        // TODO add your handling code here:
        editar = true;

        int fila = tblActividad.getSelectedRow();

        if (fila >= 0) {

            txtCodigo.setText(tblActividad.getValueAt(fila, 0).toString());

            txtNombre.setText(tblActividad.getValueAt(fila, 1).toString());
        }
    }//GEN-LAST:event_tblActividadMouseClicked

