    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        String id = javax.swing.JOptionPane.showInputDialog(
                this,
                "Ingrese el código del activo a buscar:",
                "Buscar activo",
                javax.swing.JOptionPane.QUESTION_MESSAGE
        );

        if (id == null) {
            return;
        }

        id = id.trim();

        if (id.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Debe ingresar un código");
            return;
        }

        Capa_persistencia.ActivoMant a = negocio.buscar(id);

        if (a != null) {
            txtId.setText(a.getCodigo());
            txtNombre.setText(a.getNombre());

            if (a.getFechaCompra() != null) {
                java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("dd/MM/yyyy");
                txtFecha.setText(sdf.format(a.getFechaCompra()));
            } else {
                txtFecha.setText("");
            }

            editar = true;

        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Activo no encontrado");
        }

    }//GEN-LAST:event_btnBuscarActionPerformed

