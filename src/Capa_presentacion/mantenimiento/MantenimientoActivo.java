    private void btnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarActionPerformed
        // TODO add your handling code here:
        String id = txtId.getText().trim();
        String nombre = txtNombre.getText().trim();
        String fecha = txtFecha.getText().trim();
        if (nombre.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Ingrese el nombre");
            return;
        }
        if (fecha.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Ingrese la fecha (dd/MM/yyyy)");
            return;
        }
        try {
            java.util.Date fechaDate = parsearFecha(fecha);
            int res = negocio.insertar(id, nombre, fechaDate);
            if (res == 1) {
                javax.swing.JOptionPane.showMessageDialog(this, "Activo insertado correctamente");
                limpiarCampos();
                cargarTabla();
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Error al insertar");
            }
        } catch (Exception ex) {
            javax.swing.JOptionPane.showMessageDialog(this, "Formato de fecha incorrecto. Use dd/MM/yyyy  Ejemplo: 15/03/2024");
        }
    }//GEN-LAST:event_btnInsertarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
        if (!editar) {
            javax.swing.JOptionPane.showMessageDialog(this, "Seleccione un registro de la tabla primero");
            return;
        }
        String id = txtId.getText().trim();
        String nombre = txtNombre.getText().trim();
        String fecha = txtFecha.getText().trim();
        if (nombre.isEmpty() || fecha.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Complete todos los campos");
            return;
        }
        try {
            java.util.Date fechaDate = parsearFecha(fecha);
            int res = negocio.modificar(id, nombre, fechaDate);
            if (res == 1) {
                javax.swing.JOptionPane.showMessageDialog(this, "Modificado correctamente");
                limpiarCampos();
                cargarTabla();
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Error al modificar");
            }
        } catch (Exception ex) {
            javax.swing.JOptionPane.showMessageDialog(this, "Formato de fecha incorrecto. Use dd/MM/yyyy");
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
       if (!editar) {
        javax.swing.JOptionPane.showMessageDialog(this, "Seleccione un registro de la tabla primero");
        return;
    }

    String id = txtId.getText().trim();

    int confirm = javax.swing.JOptionPane.showConfirmDialog(
            this,
            "¿Eliminar el activo: " + txtNombre.getText() + "?",
            "Confirmar",
            javax.swing.JOptionPane.YES_NO_OPTION
    );

    if (confirm == javax.swing.JOptionPane.YES_OPTION) {
        int res = negocio.eliminar(id);

        if (res == 1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Eliminado correctamente");
            limpiarCampos();
            cargarTabla();
        } else {
            javax.swing.JOptionPane.showMessageDialog(this,
                    "No se pudo eliminar. El activo está usado en mantenimientos.");
        }
    }
    }//GEN-LAST:event_btnEliminarActionPerformed

