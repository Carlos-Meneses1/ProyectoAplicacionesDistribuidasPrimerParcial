    private void cargarTabla() {
        javax.swing.table.DefaultTableModel model = new javax.swing.table.DefaultTableModel(
                new String[]{"ID", "Nombre", "Fecha Compra"}, 0);
        java.util.List<Capa_persistencia.ActivoMant> lista = negocio.listar();
        if (lista != null) {
            for (Capa_persistencia.ActivoMant a : lista) {
                String fecha = "";
                if (a.getFechaCompra() != null) {
                    java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("dd/MM/yyyy");
                    fecha = sdf.format(a.getFechaCompra());
                }
                model.addRow(new Object[]{a.getCodigo(), a.getNombre(), fecha});
            }
        }
        jTable2.setModel(model);
    }

    private void limpiarCampos() {
        txtId.setText(negocio.siguienteId());
        txtNombre.setText("");
        txtFecha.setText("");
        editar = false;
    }

    private void seleccionarActivo() {
        editar = true;

        int fila = jTable2.getSelectedRow();

        if (fila >= 0) {
            String id = jTable2.getValueAt(fila, 0).toString();

            Capa_persistencia.ActivoMant a = negocio.buscar(id);

            if (a != null) {
                txtId.setText(a.getCodigo());
                txtNombre.setText(a.getNombre());

                if (a.getFechaCompra() != null) {
                    java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("dd/MM/yyyy");
                    txtFecha.setText(sdf.format(a.getFechaCompra()));
                } else {
                    txtFecha.setText("");
                }
            }
        }
    }

    private java.util.Date parsearFecha(String texto) throws Exception {
        java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("dd/MM/yyyy");
        return sdf.parse(texto);
    }
