    private void nuevoCodigo() {
        txtCodigo.setText(negocio.siguienteId());
        txtNombre.setText("");
        txtNombre.requestFocus();
    }

    private void cargarTabla() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Código");
        modelo.addColumn("Nombre");

        List<TipoActivo> lista = negocio.listar();

        if (lista != null) {
            for (TipoActivo t : lista) {
                modelo.addRow(new Object[]{
                    t.getCodigo(),
                    t.getNombre()
                });
            }
        }

        tblActividad.setModel(modelo);
    }

    private boolean validarCampos() {
        if (txtNombre.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese el nombre del tipo de activo");
            txtNombre.requestFocus();
            return false;
        }
        return true;
    }

