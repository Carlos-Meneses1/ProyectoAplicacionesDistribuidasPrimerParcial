    private void btnNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevoActionPerformed
        // TODO add your handling code here:
        if (!validarCampos()) {
            return;
        }

        int r = negocio.insertar(
                txtCodigo.getText(),
                txtNombre.getText().trim()
        );

        if (r == 1) {
            JOptionPane.showMessageDialog(this, "Tipo de activo insertado correctamente");
            cargarTabla();
            nuevoCodigo();
        } else {
            JOptionPane.showMessageDialog(this, "Error al insertar");
        }
    }//GEN-LAST:event_btnNuevoActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
        if (!validarCampos()) {
            return;
        }

        int r = negocio.modificar(
                txtCodigo.getText(),
                txtNombre.getText().trim()
        );

        if (r == 1) {
            JOptionPane.showMessageDialog(this, "Tipo de activo modificado correctamente");
            cargarTabla();
            nuevoCodigo();
        } else {
            JOptionPane.showMessageDialog(this, "No se pudo modificar");
        }

    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        int opcion = JOptionPane.showConfirmDialog(
                this,
                "¿Seguro que desea eliminar este tipo de activo?",
                "Confirmar",
                JOptionPane.YES_NO_OPTION
        );

        if (opcion == JOptionPane.YES_OPTION) {
            int r = negocio.eliminar(txtCodigo.getText());

            if (r == 1) {
                JOptionPane.showMessageDialog(this, "Tipo de activo eliminado correctamente");
                cargarTabla();
                nuevoCodigo();
            } else {
                JOptionPane.showMessageDialog(this, "No se pudo eliminar");
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        String codigo = JOptionPane.showInputDialog(this, "Ingrese el código:");

        if (codigo != null && !codigo.trim().isEmpty()) {
            TipoActivo t = negocio.buscar(codigo.trim());

            if (t != null) {
                txtCodigo.setText(t.getCodigo());
                txtNombre.setText(t.getNombre());
            } else {
                JOptionPane.showMessageDialog(this, "No existe el tipo de activo");
            }
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void tblActividadMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblActividadMouseClicked
        // TODO add your handling code here:
        int fila = tblActividad.getSelectedRow();

        if (fila >= 0) {
            txtCodigo.setText(tblActividad.getValueAt(fila, 0).toString());
            txtNombre.setText(tblActividad.getValueAt(fila, 1).toString());
        }

    }//GEN-LAST:event_tblActividadMouseClicked

