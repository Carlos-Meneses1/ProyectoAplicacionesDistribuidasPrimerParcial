private void nuevoId() {
    txtId1.setText(negocio.siguienteId());
    txtNombre1.setText("");
    txtPeriodos1.setText("");
    txtValorCompra1.setText("");

    if (cmbTipoActivo1.getItemCount() > 0) {
        cmbTipoActivo1.setSelectedIndex(0);
    }

    txtNombre1.requestFocus();
}

private void cargarComboTipoActivo() {
    cmbTipoActivo1.removeAllItems();

    List<TipoActivo> lista = negocio.listarTiposActivo();

    if (lista != null) {
        for (TipoActivo t : lista) {
            cmbTipoActivo1.addItem(t.getCodigo() + " - " + t.getNombre());
        }
    }
}

private void cargarTabla() {
    DefaultTableModel modelo = new DefaultTableModel();
    modelo.addColumn("ID");
    modelo.addColumn("Nombre");
    modelo.addColumn("Periodos");
    modelo.addColumn("Valor compra");
    modelo.addColumn("Tipo activo");

    List<Activo> lista = negocio.listar();

    if (lista != null) {
        for (Activo a : lista) {
            String tipo = "";

            if (a.getCodTipo() != null) {
                tipo = a.getCodTipo().getNombre();
            }

            modelo.addRow(new Object[]{
                a.getId(),
                a.getNombre(),
                a.getPeriodosDepreciacion(),
                a.getValorCompra(),
                tipo
            });
        }
    }

    jTable2.setModel(modelo);
}

private boolean validarCampos() {
    if (txtNombre1.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Ingrese el nombre del activo");
        txtNombre1.requestFocus();
        return false;
    }

    if (txtPeriodos1.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Ingrese los periodos de depreciación");
        txtPeriodos1.requestFocus();
        return false;
    }

    if (txtValorCompra1.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Ingrese el valor de compra");
        txtValorCompra1.requestFocus();
        return false;
    }

    if (cmbTipoActivo1.getSelectedItem() == null) {
        JOptionPane.showMessageDialog(this, "Primero debe registrar un tipo de activo");
        return false;
    }

    try {
        Integer.parseInt(txtPeriodos1.getText().trim());
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Los periodos deben ser un número entero");
        txtPeriodos1.requestFocus();
        return false;
    }

    try {
        new BigDecimal(txtValorCompra1.getText().trim());
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "El valor de compra debe ser numérico");
        txtValorCompra1.requestFocus();
        return false;
    }

    return true;
}

private String obtenerCodigoTipoSeleccionado() {
    String texto = cmbTipoActivo1.getSelectedItem().toString();
    String codigo = texto.substring(0, texto.indexOf(" - "));
    return codigo;
}

private void seleccionarTipoActivo(String codigo) {
    for (int i = 0; i < cmbTipoActivo1.getItemCount(); i++) {
        String item = cmbTipoActivo1.getItemAt(i).toString();

        if (item.startsWith(codigo + " - ")) {
            cmbTipoActivo1.setSelectedIndex(i);
            return;
        }
    }
}
