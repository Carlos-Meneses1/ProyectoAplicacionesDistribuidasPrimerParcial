    private void btnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarActionPerformed
        // TODO add your handling code here:
          if (!validarCampos()) {
        return;
    }

    String codTipo = obtenerCodigoTipoSeleccionado();

    int r = negocio.insertar(
            txtId1.getText(),
            txtNombre1.getText().trim(),
            Integer.parseInt(txtPeriodos1.getText().trim()),
            new BigDecimal(txtValorCompra1.getText().trim()),
            codTipo
    );

    if (r == 1) {
        JOptionPane.showMessageDialog(this, "Activo insertado correctamente");
        cargarTabla();
        nuevoId();
    } else {
        JOptionPane.showMessageDialog(this, "Error al insertar el activo");
    }
      
    }//GEN-LAST:event_btnInsertarActionPerformed

