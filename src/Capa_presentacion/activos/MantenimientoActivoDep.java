    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
          int opcion = JOptionPane.showConfirmDialog(
            this,
            "¿Seguro que desea eliminar este activo?",
            "Confirmar",
            JOptionPane.YES_NO_OPTION
    );

    if (opcion == JOptionPane.YES_OPTION) {
        int r = negocio.eliminar(txtId1.getText());

        if (r == 1) {
            JOptionPane.showMessageDialog(this, "Activo eliminado correctamente");
            cargarTabla();
            nuevoId();
        } else {
            JOptionPane.showMessageDialog(this, "No se pudo eliminar el activo");
        }
    }
    
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
       String id = JOptionPane.showInputDialog(this, "Ingrese el ID del activo:");

    if (id != null && !id.trim().isEmpty()) {
        Activo a = negocio.buscar(id.trim());

        if (a != null) {
            txtId1.setText(a.getId());
            txtNombre1.setText(a.getNombre());

            if (a.getPeriodosDepreciacion() != null) {
                txtPeriodos1.setText(a.getPeriodosDepreciacion().toString());
            } else {
                txtPeriodos1.setText("");
            }

            if (a.getValorCompra() != null) {
                txtValorCompra1.setText(a.getValorCompra().toString());
            } else {
                txtValorCompra1.setText("");
            }

            if (a.getCodTipo() != null) {
                seleccionarTipoActivo(a.getCodTipo().getCodigo());
            }

        } else {
            JOptionPane.showMessageDialog(this, "No existe el activo");
        }
    }
    }//GEN-LAST:event_btnBuscarActionPerformed

